fun main(args: Array<String>) {
    val x = 2
    val y = 4

    // Equality operator
    println(x == y)

    // Inequality operator (as in the original code)
    println(x != y)

    // Greater than operator
    println(x > y)

    // Less than operator
    println(x < y)

    // Greater than or equal to operator
    println(x >= y)

    // Less than or equal to operator
    println(x <= y)

    // Logical AND operator
    println((x > 0) && (y > 0))

    // Logical OR operator
    println((x > 0) || (y > 0))

    // Logical NOT operator
    println(!(x > 0))

    /*
    += sum
    -= subtract
    /= divide
    *= multiply
    */

}
